const lampuLaluLintas = (warna) => {
  if (warna === "hijau") {
    console.log("jalan");
  } else if (warna === "kuning") {
    console.log("hati-hati");
  } else if (warna === "merah") {
    console.log("berhenti");
  } else {
    console.log("lampu error");
  }
};

lampuLaluLintas("merah");

   